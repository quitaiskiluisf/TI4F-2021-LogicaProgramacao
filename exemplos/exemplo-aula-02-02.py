# Apresentação
print("Programa para calcular a área de um retângulo")

# Solicita os valores de entrada
lado_a = float(input("Informe a medida do lado A: "))
lado_b = float(input("Informe a medida do lado B: "))

# Processamento
resultado = lado_a * lado_b

# Apresentação das saídas
print("A área do retângulo é:", resultado)
