# Apresentação
print('Programa para calcular a média de um aluno')

# Entradas
n1 = float(input('Informe a nota 1: '))
n2 = float(input('Informe a nota 2: '))
n3 = float(input('Informe a nota 3: '))

# Processamento
media = (n1 + n2 + n3) / 3

# Saídas
print('A média do aluno é:', media)
