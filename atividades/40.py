def dobro_menos_um(numero):
    return numero * 2 - 1


# Apresentação
print('Programa para calcular o dobro menos 1 de um valor')
print()

# Entradas
v = int(input('Informe um valor: '))

# Processamento
resultado = dobro_menos_um(numero=v)

# Saídas
print()
print('O resultado é', resultado)
