# Apresentação
print("Programa para calcular a área de um triangulo")

# Solicita os valores de entrada
base = float(input("Informe a medida da base: "))
altura = float(input("Informe a medida da altura: "))

# Processamento
resultado = base * altura / 2

# Apresentação das saídas
print("A área do triângulo é:", resultado)
